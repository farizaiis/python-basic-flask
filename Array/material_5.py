fruits = ['Apple', 'Banana', 'Mango', 'Grapes', 'Orange']
print(fruits)
# current Fruits = ['Apple', 'Banana', 'Mango', 'Grapes', 'Orange']
fruits[1] = 'Pineapple' # Change Banana to Pineapple (index 1)
print(fruits) 
# current Fruits = ['Apple', 'Pineapple', 'Mango', 'Grapes', 'Orange']
fruits[-1] = 'Guava' # Change Orange to Guava (last index)
print(fruits)
# current Fruits = ['Apple', 'Pineapple', 'Mango', 'Grapes', 'Guava']