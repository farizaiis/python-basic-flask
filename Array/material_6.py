# Concating 2 Array using + operator
concat = [1, 2, 3]
print(concat)
# Output = [1, 2, 3]

concat2 = [4, 5, 6]
print(concat2)
# Output = [4, 5, 6]

concat3 = concat + concat2
print(concat3)
# Output = [1, 2, 3, 4, 5, 6]


# Repeating index on array
repeat = ['a']
repeat = repeat * 5
print(repeat)
# Output = ['a', 'a', 'a', 'a', 'a']